    DATASET = REPO_TYPE_DATASET
    SPACE = REPO_TYPE_SPACE
    CODE = REPO_TYPE_CODE
    MCP = REPO_TYPE_MCPSERVER
    SKILL = REPO_TYPE_SKILL
